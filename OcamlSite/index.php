    <h1>About OcaIDE</h1>

    OcaIDE is an <a href="http://caml.inria.fr/ocaml/index.en.html">OCaml</a> plug-in for <a href="http://www.eclipse.org/">Eclipse</a>.

    <p>

        It is distributed under the <a href="http://www.cecill.info/licences/Licence_CeCILL-B_V1-en.html">CeCILL-B</a> Open Source licence.

    <p>

        Its name is a contraction of <strong>Oca</strong>ml and <strong>IDE</strong>.

    <p>

        It has the following features:
    <ul>
        <li> Source editor for modules (ml files), interfaces (mli files), parsers (mly files) and lexers (mll files)
        <li> Syntax coloring (colors and styles are configurable)
        <li> Automatic indentation while typing in the editor (configurable in the preferences)
        <li> A customizable integrated code formatter, and an interface to the camlp4 formatter (through an AST printer)
        <li> Completion
        <li> Library browser, both for the standard library and user libraries
        <li> Integrated debugger (a graphical interface to the OCaml text-mode debugger), with remote debugging support and script file support
        <li> Highlighting of matching delimiters
        <li> Integrated toplevel
        <li> Creation of custom toplevels
        <li> Camlp4 pre-processing
        <li> An OCaml perspective inside of Eclipse
        <li> Shortcuts to the OCaml elements in Eclipse
        <li> Building of OCaml projects (with ocamlbuild, with hand-written makefile, with OcamlMakefile or managed by the IDE)
        <li> Importation and exportation of OCaml projects
        <li> Launching of OCaml executables with parameters, and interactive console
        <li> Outline and Quick Outline
        <li> Displays the inferred type of elements in the editor and outline
        <li> Descriptive popups on OCaml definitions
        <li> Help on expected parameters for functions, exceptions and constructors
        <li> Configurable shortcuts
        <li> Configurable paths for all the tools (and the paths are automatically detected)
        <li> On demand makefile generation
        <li> Automatic interface generation
        <li> Hyperlinks for jumping to the definition of an element (variable, type constructor...)
        <li> Error markers in the editor, editor's margin, Navigator view and Problems view
        <li> Editable code templates
        <li> Automatic detection of project paths
        <li> Commenting and uncommenting of code blocks
        <li> Spell checking of comments and documentation comments
        <li> Switch between module/interface
        <li> Convert files between revised syntax and standard syntax (with Camlp4)
    </ul>

